/// Detect the project's tech stack by checking for well-known files.
pub fn detect_tech_stack(project_dir: &std::path::Path) -> Vec<String> {
    let checks: &[(&str, &str)] = &[
        ("package.json", "node.js"),
        ("next.config.js", "next.js"),
        ("next.config.ts", "next.js"),
        ("next.config.mjs", "next.js"),
        ("prisma/schema.prisma", "prisma"),
        ("Cargo.toml", "rust"),
        ("docker-compose.yml", "docker"),
        ("docker-compose.yaml", "docker"),
        ("Dockerfile", "docker"),
        ("flake.nix", "nixos"),
        ("requirements.txt", "python"),
        ("pyproject.toml", "python"),
        ("go.mod", "go"),
        ("tsconfig.json", "typescript"),
    ];

    let mut stack = Vec::new();
    for &(file, tech) in checks {
        if project_dir.join(file).exists() && !stack.contains(&tech.to_string()) {
            stack.push(tech.to_string());
        }
    }
    stack
}

/// Build a cross-project knowledge section by finding projects with overlapping tech stacks.
async fn build_cross_project_section(
    pool: &PgPool,
    current_project_id: Option<&str>,
    current_tech_stack: &[String],
) -> Result<Section> {
    if current_tech_stack.is_empty() {
        return Ok(Section {
            heading: "Related Knowledge (Other Projects)",
            body: String::new(),
            priority: 6,
        });
    }

    let projects = ProjectRepo::list(pool).await.unwrap_or_default();
    let mut body = String::new();
    let mut items_added = 0usize;
    let max_items = 5;

    for project in &projects {
        if current_project_id == Some(project.id.as_str()) {
            continue;
        }
        if items_added >= max_items {
            break;
        }

        // Check tech stack overlap via core memories
        let filter = ListCoreMemoryFilter {
            category: Some("fact".to_string()),
            project: Some(project.id.clone()),
            limit: Some(50),
            ..Default::default()
        };
        let memories = CoreMemoryRepo::list(pool, &filter)
            .await
            .unwrap_or_default();
        let has_overlap = memories.iter().any(|m| {
            let lower = m.value.to_lowercase();
            current_tech_stack.iter().any(|tech| lower.contains(tech))
        });

        if !has_overlap {
            continue;
        }

        // Get notable episodes from this project
        let ep_filter = ListEpisodesFilter {
            project: Some(project.id.clone()),
            kind: Some("error".to_string()),
            limit: Some(3),
            ..Default::default()
        };
        let episodes = EpisodeRepo::list(pool, &ep_filter)
            .await
            .unwrap_or_default();

        for ep in episodes.iter().take(2) {
            if items_added >= max_items {
                break;
            }
            let snippet = truncate_str(&ep.content, 120);
            use std::fmt::Write;
            let _ = writeln!(body, "- **{}** ({}): {}", ep.title, project.name, snippet);
            items_added += 1;
        }
    }

    Ok(Section {
        heading: "Related Knowledge (Other Projects)",
        body,
        priority: 6,
    })
}

/// Truncate a string to at most `max_len` characters, appending "..." if truncated.
/// Uses `floor_char_boundary` for UTF-8 safety.
fn truncate_str(s: &str, max_len: usize) -> String {
    if s.len() <= max_len {
        return s.to_string();
    }
    let boundary = s.floor_char_boundary(max_len);
    format!("{}...", &s[..boundary])
}

/// Map severity strings to a numeric rank for sorting (lower = more severe).
fn severity_rank(severity: Option<&str>) -> u8 {
    match severity {
        Some("critical") => 0,
        Some("high") => 1,
        Some("medium") => 2,
        Some("low") => 3,
        _ => 4,
    }
}
